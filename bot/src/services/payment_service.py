"""Сервис оплаты.

Сейчас это заглушка: она возвращает тестовую ссылку. Позже здесь нужно
подключить официальный платёжный провайдер и создавать реальные платежи.
"""

from src.models.order import Order
from src.config import settings


class PaymentService:
    def build_payment_url(self, order: Order) -> str | None:
        # В dev-режиме не создаем даже тестовую внешнюю ссылку.
        # Вместо нее handler покажет Telegram-кнопку "Тестовая оплата",
        # которая просто переведет заявку в paid через внутренний callback.
        if settings.is_dev:
            return None

        # В production здесь позже будет API-запрос на создание платежа.
        # Пока оставляем стабильную ссылку-заглушку, чтобы интерфейс не ломался
        # до подключения реального провайдера.
        #
        # Для официальной оплаты в РФ лучше делать так:
        # 1. создать платеж на сервере через API провайдера, например ЮKassa;
        # 2. передать сумму, валюту RUB, описание заказа и return_url;
        # 3. сохранить payment_id и confirmation_url в базе;
        # 4. менять статус заявки на paid только после webhook от провайдера.
        #
        # Нажатие пользователем на кнопку оплаты не является доказательством оплаты:
        # пользователь может закрыть страницу, платеж может быть отменен или не пройти 3-D Secure.
        return f"https://example.com/pay/order/{order.id}"
